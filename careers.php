<p><strong>About Us:</strong></p>
<p>Setfive Consulting is a four year old Boston based software
consulting firm with dozens of clients across several industries. Our
primary focus is working with companies to architect, build, and support
new Internet ventures and products. In addition, we internally build
projects which sometimes are "spun off" into separate entities.</p>

<p>Technically, we're primarily a LAMP focused firm and prefer to use
PHP along with the Symfony framework or Drupal. On the frontend, we use
jQuery exclusively and often use the Twitter Bootstrap framework along
with BackboneJS.</p>

<a name="juniorLAMP"></a>
<h3>Junior LAMP Developer</h3>
<div class="big-sep"></div>

<p>At a high level, you'll be joining our team as a junior developer to
work with our clients to tackle their problems and help them achieve
their business goals. In doing so, you'll get the opportunity to work
with a diverse set of clients and be involved in projects at every stage
of their life cycle. In addition, you'll be expected to manage your own
projects, interact professionally with clients, and deliver quality code
on schedule.</p>

<p>You'll work with clients through the entire development process from
transforming business goals into a technical specification and finally
building the product. While doing this, you'll get exposed to the latest
technology and have the freedom to select the right tool for the job
while accounting for business constraints.</p>

<p>Throughout this process, you'll work closely with a senior developer
who will serve as both a mentor and guide to help accelerate your
professional growth with the goal of you joining our senior engineering
staff.</p>

<p>
<strong>Responsibilities:</strong>
<ul>
  <li>Help clients create software to achieve their business goals.</li>
  <li>Explain technical concepts to non-technical individuals.</li>
  <li>Develop and ship solid code on the LAMP stack.</li>
</ul>
</p>

<p>
<strong>Experience and Skills:</strong>
<ul>
  <li>1-2 years developing code on the LAMP stack</li>
  <li>Modern PHP (v5.2+)</li>
  <li>Familiarity with MySQL</li>
  <li>Javascript with either jQuery (preferred) or Prototype</li>
  <li>HTML/CSS</li>
  <li>Source control, preferably git</li>
  <li>Experience building MVC driven web applications using a modern framework would be a plus</li>
  <li>Previous experience interacting with clients would be a definite plus</li>
</ul>
</p>

<p>
<strong>Compensation/Perks:</strong>
    <ul>
    <li>Depending on experience, market rate with an additional performance bonus.</li>
    <li>End of year bonus based on overall revenue</li>
    <li>Paid health care</li> 
    <li>401(k)</li>
    <li>Open schedule - set your own hours</li>
    <li>Option to telecommute</li>
    <li>Generous paid time off</li>
    <li>Sponsored company outings </li>
    <li>Personal development time to experiment with new technologies or work on your own projects</li>  
  </ul>
</p>

